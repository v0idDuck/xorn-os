#include "display.h"

static UINT32* fb;
static int     fb_width;
static int     fb_height;

void display_init(EFI_GRAPHICS_OUTPUT_PROTOCOL* gop) {
    fb        = (UINT32*)gop->Mode->FrameBufferBase;
    fb_width  = gop->Mode->Info->HorizontalResolution;
    fb_height = gop->Mode->Info->VerticalResolution;
}

void display_clear(UINT32 color) {
    int total = fb_width * fb_height;
    for (int i = 0; i < total; i++)
        fb[i] = color;
}

void display_pixel(int x, int y, UINT32 color) {
    if (x < 0 || y < 0) return;
    if (x >= fb_width || y >= fb_height) return;
    fb[y * fb_width + x] = color;
}

void display_rect(int x, int y, int w, int h, UINT32 color) {
    for (int py = y; py < y + h; py++)
        for (int px = x; px < x + w; px++)
            display_pixel(px, py, color);
}

