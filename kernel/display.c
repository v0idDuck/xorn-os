void display_char(int x, int y, char c, UINT32 color, int scale) {
    if (c < 0 || c > 127) return;
    unsigned char* glyph = font[(unsigned char)c];
    for (int row = 0; row < 7; row++) {
        for (int col = 0; col < 5; col++) {
            if (glyph[row] & (1 << (4 - col))) {
                // рисуем scale x scale блок вместо одного пикселя
                display_rect(x + col * scale, y + row * scale,
                             scale, scale, color);
            }
        }
    }
}

void display_text(int x, int y, const char* text, UINT32 color, int scale) {
    int cx = x;
    while (*text) {
        display_char(cx, y, *text, color, scale);
        cx += 6 * scale;  // отступ тоже масштабируем
        text++;
    }
}
// display.c
int cursor_x = 5;
int cursor_y = 5;

// print — без переноса строки
void display_print(const char* text, UINT32 color) {
    display_text(cursor_x, cursor_y, text, color, font_scale);
    while (*text++) cursor_x += 6 * font_scale;
}

// println — с переносом строки
void display_println(const char* text, UINT32 color) {
    display_text(cursor_x, cursor_y, text, color, font_scale);
    cursor_x = 0;
    cursor_y += FONT_H;
}